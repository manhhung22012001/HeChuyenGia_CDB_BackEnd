package com.Hechuyengia.Chuandoanbenh.DTO;

/**
 *
 * @author tranm
 */
public class EmailOtpDTO {
    private String email;
    private String otp;

    public EmailOtpDTO() {
    }

    public EmailOtpDTO(String email, String otp) {
        this.email = email;
        this.otp = otp;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getOtp() {
        return otp;
    }

    public void setOtp(String otp) {
        this.otp = otp;
    }
    
}
